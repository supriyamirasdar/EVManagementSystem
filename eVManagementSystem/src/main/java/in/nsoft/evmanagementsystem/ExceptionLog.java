package in.nsoft.evmanagementsystem;
/**
 * 
 * @author Nitish
 * @Desc  Object for ExceptionLoging
 * @Started  14-01-2016
 */
public class ExceptionLog{
	
	private String mScreenName = "";
	private String mExceptionNo = "";
	private String mExceptionDesc = "";
	private String mExceptionDateTime="";
	private String mGPRSFlag="";
	
	
	public String getmScreenName() {
		return mScreenName;
	}
	public void setmScreenName(String mScreenName) {
		this.mScreenName = mScreenName;
	}
	public String getmExceptionNo() {
		return mExceptionNo;
	}
	public void setmExceptionNo(String mExceptionNo) {
		this.mExceptionNo = mExceptionNo;
	}
	public String getmExceptionDesc() {
		return mExceptionDesc;
	}
	public void setmExceptionDesc(String mExceptionDesc) {
		this.mExceptionDesc = mExceptionDesc;
	}
	public String getmExceptionDateTime() {
		return mExceptionDateTime;
	}
	public void setmExceptionDateTime(String mExceptionDateTime) {
		this.mExceptionDateTime = mExceptionDateTime;
	}
	public String getmGPRSFlag() {
		return mGPRSFlag;
	}
	public void setmGPRSFlag(String mGPRSFlag) {
		this.mGPRSFlag = mGPRSFlag;
	}
				

}
