package in.nsoft.evmanagementsystem;
/**
 * 
 * @author Nitish
 * @Desc  This class will contain details of a logged in user
 * @Started  14-01-2016
 */
public class UserDetails  {
	
	private String mID;
	private String mUserName;
	private String mUserCode;
	private String mDesignation;
	private String mEmailId;	
	private String mMobileNo;
	public String getmID() {
		return mID;
	}
	public void setmID(String mID) {
		this.mID = mID;
	}
	public String getmUserName() {
		return mUserName;
	}
	public void setmUserName(String mUserName) {
		this.mUserName = mUserName;
	}
	public String getmUserCode() {
		return mUserCode;
	}
	public void setmUserCode(String mUserCode) {
		this.mUserCode = mUserCode;
	}
	public String getmDesignation() {
		return mDesignation;
	}
	public void setmDesignation(String mDesignation) {
		this.mDesignation = mDesignation;
	}
	public String getmEmailId() {
		return mEmailId;
	}
	public void setmEmailId(String mEmailId) {
		this.mEmailId = mEmailId;
	}
	public String getmMobileNo() {
		return mMobileNo;
	}
	public void setmMobileNo(String mMobileNo) {
		this.mMobileNo = mMobileNo;
	}
	
	

}
