package in.nsoft.evmanagementsystem;

public class UserData {
	private String mUserName;
	private String mPassword;	
	private int mRegistered;	
	private String mSyncDate;	
	private String mLatitude;	
	private String mLongitude;	
	private String mMobileNo;	
	
	public String getmUserName() {
		return mUserName;
	}
	public void setmUserName(String mUserName) {
		this.mUserName = mUserName;
	}
	public String getmPassword() {
		return mPassword;
	}
	public void setmPassword(String mPassword) {
		this.mPassword = mPassword;
	}
	
	public String getmSyncDate() {
		return mSyncDate;
	}
	public void setmSyncDate(String mSyncDate) {
		this.mSyncDate = mSyncDate;
	}
	public String getmLatitude() {
		return mLatitude;
	}
	public void setmLatitude(String mLatitude) {
		this.mLatitude = mLatitude;
	}
	public String getmLongitude() {
		return mLongitude;
	}
	public void setmLongitude(String mLongitude) {
		this.mLongitude = mLongitude;
	}
	public String getmMobileNo() {
		return mMobileNo;
	}
	public void setmMobileNo(String mMobileNo) {
		this.mMobileNo = mMobileNo;
	}
	public int getmRegistered() {
		return mRegistered;
	}
	public void setmRegistered(int mRegistered) {
		this.mRegistered = mRegistered;
	}
	
	
}